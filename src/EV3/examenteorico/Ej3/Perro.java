package EV3.examenteorico.Ej3;

public class Perro {
    String nombre;
    String raza;

    public Perro (String nombre, String raza){
        this.nombre = nombre;
        this.raza = raza;
    }

    public String getNombre() {
        return nombre;
    }

    public String getRaza() {
        return raza;
    }
}
